package com.capgemini.ccsw.codingdojo.lunarrover;

/**
 *
 */
public class Point {
    private int x;

    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * @return x
     */
    public int getX() {

        return this.x;
    }

    /**
     * @return y
     */
    public int getY() {

        return this.y;
    }

}
